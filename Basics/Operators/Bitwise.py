"""""
Can apply  for int and bool type


<<,>>,^,~,|, &

"""

print(4^6)   # If bits are different then result is 1 otherwise 0


print(True & True)  # If both are is not zero(False) o/p will be True otherwise False

print(bool(0|0))  # If any digit is not zero o/p will be True otherwise False

print(~100)  # Bitwise complement

print(4<<2)  # Left shift in binary form

print(4>>2)  # Right shift in binary form

