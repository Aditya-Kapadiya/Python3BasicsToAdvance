# Changing the case of tring

x = 'abasdD sdja fbA'

print(x.upper())  # It will make upper case all the string elements

print(x.lower())  # It will make lower case all the string elements

print(x.swapcase())  # It will reverse the cases of each elements loweer to upper and upper to lower

print(x.title())   # To make the all first items of a word to uppercase

print(x.capitalize())   # To make only fist  letter capital

