#  We can format the string  by using replacement operator {} and format()

name = 'Vivek'
salary = 23000
experience = 1

print('{} is having {}  year of experience and his  salary is {} INR'.format(name,experience,salary))

print('{x} is having {y}  year of experience and his  salary is {z} INR'.format(x =name,y =experience,z=salary))
